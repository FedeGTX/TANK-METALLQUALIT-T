            El sistema de advertencia láser ISTEC (LWS) es una mejora de la capacidad de bajo costo, ya sea para vehículos nuevos o vehículos que necesitan un programa de actualización/extensión de vida útil. El sistema ISTEC se compone de dos elementos, el conjunto del detector y el panel de visualización (también se pueden suministrar arneses de interconexión y alimentación). Originalmente concebido como un complemento del sistema de descarga de granadas de 66 mm ISTEC en servicio, el LWS ha sido diseñado para usarse también como un sistema independiente. Una versión posterior del sistema ahora incluye múltiples conjuntos de detectores para su instalación en plataformas más grandes.

El LWS tiene una resolución de 22,5° y está diseñado para detectar la dirección de una radiación láser, por ejemplo, de láseres de un solo pulso (telémetros) o sistemas de pulsos múltiples (designadores) y a través del panel de visualización, alertará al usuario del vehículo visualmente. y audiblemente. El conjunto de detector único se compone de sensores x8, más un sensor superior. Cada sensor emplea una implementación de detector dual, que brinda sensibilidad a longitudes de onda entre 730 y 1700 nm.

La opción de conjunto de detectores múltiples está diseñada para que, idealmente, un mínimo de cuatro conjuntos de detectores estén ubicados estratégicamente en la plataforma para garantizar una cobertura de 360°. Cada uno de estos conjuntos de detectores se compone de x5 sensores detectores dobles, más un sensor superior.
            
