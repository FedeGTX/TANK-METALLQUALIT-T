            La munición HEAT (High Explosive Anti-Tank, por sus siglas en inglés) es un tipo de proyectil utilizado para combatir vehículos blindados, especialmente tanques. Está diseñada para penetrar la armadura de los vehículos y causar daño significativo en su interior.

El proyectil HEAT se compone de un cono hueco en su parte frontal, una carga explosiva en su interior y una base estabilizadora en la parte trasera. Cuando el proyectil impacta contra un objetivo, la carga explosiva se detona, creando una intensa corriente de gas y calor concentrada en el cono hueco.

Este cono hueco actúa como un proyectil en sí mismo al ser disparado. Al detonar, el gas y el calor generados en el cono hueco forman un chorro de alta velocidad conocido como "chorro de metal" o "jet", que se dirige hacia la superficie del objetivo blindado. Este chorro de metal, impulsado por la carga explosiva, tiene una alta temperatura y energía cinética, lo que le permite perforar la armadura y crear un canal de penetración en el vehículo.

Una vez que el chorro de metal penetra en el vehículo, puede causar daños significativos al afectar a componentes internos críticos, como el motor, la tripulación, los sistemas de armamento o el compartimiento de almacenamiento de municiones. El daño causado por el proyectil HEAT no depende de la velocidad del proyectil en sí, sino de la energía y la capacidad de penetración del chorro de metal.

Es importante tener en cuenta que la eficacia de la munición HEAT puede verse afectada por la distancia entre el proyectil y el objetivo. A medida que la distancia aumenta, el chorro de metal se dispersa y pierde energía, lo que reduce su capacidad de penetración.

La munición HEAT ha sido ampliamente utilizada en conflictos militares debido a su capacidad para enfrentar vehículos blindados. Sin embargo, con los avances en tecnología de blindaje, se han desarrollado contramedidas y mejoras en la protección que pueden reducir la eficacia de este tipo de munición. En respuesta a ello, se han desarrollado otras tecnologías de munición, como los proyectiles APFSDS (Armor-Piercing Fin-Stabilized Discarding Sabot), que ofrecen una mayor capacidad de penetración en distancias más largas.
