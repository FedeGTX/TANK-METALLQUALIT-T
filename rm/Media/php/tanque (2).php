            <P>El BMP-3 es un vehículo de combate de infantería ruso que entró en servicio en 1987. Con un peso de 18 toneladas y una tripulación de tres personas, puede transportar hasta siete soldados.

Cuenta con un sistema de armamento principal que consta de un cañón automático de 30 mm y un lanzagranadas de 100 mm capaz de disparar una amplia gama de municiones, incluyendo proyectiles antitanque y granadas de humo. Además, tiene dos ametralladoras de 7,62 mm montadas en la torreta y una en la parte frontal del vehículo.

